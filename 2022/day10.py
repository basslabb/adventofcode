from itertools import chain


def get_instruction(line):
    if "noop" in line:
        return [0]
    _, value = line.split()
    return [0, int(value)]


def main(l):
    instructions = list(chain(*[get_instruction(x) for x in l]))
    signal_strengths = []
    x_register = 1
    indexes = [20, 60, 100, 140, 180, 220]
    for i in range(1, len(instructions)):
        instruction = instructions[i-1]
        signal_strengths.append(x_register*(i))
        x_register += instruction

    print(sum([signal_strengths[i-1]
               for i in range(0, len(signal_strengths)) if i in indexes]))


